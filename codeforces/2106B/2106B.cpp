void solve()
{
  int n, x;
  cin >> n >> x;

  for (int i = 0; i < x; i++)
    cout << i << " ";
  for (int i = n - 1; i >= x; i--)
    cout << i << " ";
  cout << "\n";
}

int main(void)
{
  optimize();
  int T;
  cin >> T;
  // T = 1;
  while (T--)
    solve();
}